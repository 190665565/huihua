package com.partner.huihua.utils.exception;
/**
 * 检查http请求参数不正确时用此异常标识
 * @author rainyhao
 * @since 2015-3-10 下午2:41:28
 */
@SuppressWarnings("serial")
public class UnsatisfiedParamException extends RuntimeException {

	public UnsatisfiedParamException() {
		super();
	}

	public UnsatisfiedParamException(String message, Throwable cause) {
		super(message, cause);
	}

	public UnsatisfiedParamException(String message) {
		super(message);
	}

	public UnsatisfiedParamException(Throwable cause) {
		super(cause);
	}

}
